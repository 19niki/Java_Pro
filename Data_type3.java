class Data_type3 
{
	public static void main(String[] args) 
	{
		boolean b = true;
		byte a    = 12;
		short s   = 123;
		int i     = 1024;
		char c    = 'a';
		float f   = 1.332f;
		long l    = 120000;
		double d  = 2.3343d;
        String st = "Niki";

		System.out.println("The boolean value is:"+ b);
		System.out.println("The byte value is:"+ a);
		System.out.println("The short value is:"+ s);
		System.out.println("The int value is:"+ i);
		System.out.println("The char value is:"+ c);
		System.out.println("The float value is:"+ f);
		System.out.println("The long value is:"+ l);
		System.out.println("The double value is:"+ d);
		System.out.println("The string value is:"+ st);

	}
}
